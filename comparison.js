// Simple comparison operation by using <, >, ==, !=, <=, >= 

var largeVariable = 10;                                     
var smallVariable = 5;  
                                                     //output
//largeVariable getter then smallerVariable                                                        
console.log(largeVariable > smallVariable)           //true

//largeVariable smaller then smallerVariable                
console.log(largeVariable < smallVariable)           //false

//largeVariable smaller or eqaul then smallerVariable                
console.log(largeVariable <= smallVariable)          //false

//largeVariable getter or equal then smallerVariable                
console.log(largeVariable >= smallVariable)          //true


//largeVariable equal to smallerVariable                
console.log(largeVariable == smallVariable)         //false

//largeVariable not equal to smallerVariable                
console.log(largeVariable != smallVariable)         //true

