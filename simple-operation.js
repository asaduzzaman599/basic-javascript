
// Simple mathematical operation by using +, -, *, /, %

let a = 12;
let b = 5;

let result = 0;

//Addition   '+'
result = a + b;                                 
console.log("Resule: 12 + 5 =", result)             //Resule: 12 + 5 = 17

//Subtraction   '-'
result = a - b;
console.log("Resule: 12 - 5 =", result)             //Resule: 12 - 5 = 7

//Multiplication   '*'
result = a * b;
console.log("Resule: 12 * 5 =", result)             //Resule: 12 * 5 = 60

//Division     '/'
result = a / b;
console.log("Resule: 12 / 5 =", result)             //Resule: 12 / 5 = 2.4

//Modulus '%'
result = a % b;
console.log("Resule: 12 % 5 =", result)             //Resule: 12 % 5 =  2
